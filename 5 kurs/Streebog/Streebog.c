// u8: a7... a0 x A: a[i] * A = uint64_t[i]
uint64_t mul_table[8][256];

void set_mul_table()
{
    for (int i = 0; i < 8; i++)
    {
        for (int j = 0; j < 256; j++)
        {
            uint64_t t = 0;
            // u8: a[0] <-> A[56 .. 63]
            for (int k = 0; k < 8; k++)
            {
                if (((j >> k) & 1))
                {
                    // 63 -8i -k =63 - (i<<3)|k
                    t ^= A[63 - (8 * i + k)];
                }
            }
            mul_table[i][j] = t;
        }
    }
}

void lps(uint8_t out[64])
{
    uint8_t in[64];
    memcpy(in, out, 64);
    uint64_t *buf = (uint64_t *)out;
    for (int id = 0; id < 8; id++)
    {
        uint64_t t = 0;
        for (int i = 0; i < 8; i++)
        {
            t ^= mul_table[i][pi[in[t_[8 * id + i]]]];
        }
        t = byteReverse64(t);
        buf[id] = t;
    }
}

struct Streebog_context
{
    uint8_t h[64];
    uint8_t N[64];
    uint8_t Sum[64];
};

void Streebog_Initialize(struct Streebog_context *ctx, int n)
{

    if (n == 512)
    {
        memset(ctx->h, 0, 64);
    }
    else if (n == 256)
    {
        memset(ctx->h, 1, 64);
    }
    memset(ctx->N, 0, 64);
    memset(ctx->Sum, 0, 64);
}

void keyDeployment(uint8_t key[13][64])
{
    lps(key[0]);

    for (int i = 1; i < 13; i++)
    {
        for (int j = 0; j < 64; j++)
            key[i][j] = key[i - 1][j] ^ C[i - 1][63 - j];
        lps(key[i]);
    }
}

void E(uint8_t key[13][64], uint8_t m[64])
{
    for (int r = 0; r < 12; r++)
    {
        // Xor key
        for (int i = 0; i < 64; i++)
            m[i] ^= key[r][i];
        lps(m);
    }
    for (int i = 0; i < 64; i++)
        m[i] ^= key[12][i];
}

// m - const
void g(uint8_t h[64], uint8_t m[64])
{
    uint8_t key[13][64];
    for (int i = 0; i < 64; i++)
        key[0][i] = h[i];
    keyDeployment(key);

    uint8_t m_1[64], m_0[64];
    memcpy(m_1, m, 64);
    memcpy(m_0, m, 64);

    E(key, m_1);
    for (int i = 0; i < 64; i++)
        h[i] ^= m_1[i] ^ m_0[i];
}

// m,N - const
void g_N(uint8_t h[64], const uint8_t m[64], const uint8_t N[64])
{
    uint8_t key[13][64];
    for (int i = 0; i < 64; i++)
        key[0][i] = h[i] ^ N[i];
    keyDeployment(key);

    uint8_t m_1[64], m_0[64];
    memcpy(m_1, m, 64);
    memcpy(m_0, m, 64);

    E(key, m_1);

    for (int i = 0; i < 64; i++)
        h[i] ^= m_1[i] ^ m_0[i];
}

