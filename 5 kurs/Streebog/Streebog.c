void add_N(uint8_t N[64], int n)
{
    int i = 0;
    while (n)
    {
        n += N[i];
        N[i] = n & 0xff;
        n >>= 8;
        i++;
    }
}

void add_Sum(uint8_t sum[64], uint8_t m[64])
{
    uint16_t t = 0;
    for (int i = 0; i < 64; i++)
    {
        t += sum[i] + m[i];
        sum[i] = t & 0xff;
        t >>= 8;
    }
}

// length: number of byte
void Streebog(uint8_t *digest, uint8_t *Mess, uint64_t length, int n)
{
    struct Streebog_context ctx;
    Streebog_Initialize(&ctx, n);
    while (length >= 64)
    {
        g_N(ctx.h, Mess, ctx.N);
        add_N(ctx.N, 512);
        add_Sum(ctx.Sum, Mess);
        Mess += 64;
        length -= 64;
    }

    // length < 64
    // padding
    uint8_t *M = (uint8_t *)malloc(64);
    memcpy(M, Mess, length);
    memcpy(M + length, PADDING, 64 - length);
    g_N(ctx.h, M, ctx.N);
    add_N(ctx.N, length * 8);
    add_Sum(ctx.Sum, M);
    g(ctx.h, ctx.N);
    g(ctx.h, ctx.Sum);

    // digest;
    if (n == 256)
        memcpy(digest, ctx.h + 32, n / 8);
    else
        memcpy(digest, ctx.h, n / 8);
}

